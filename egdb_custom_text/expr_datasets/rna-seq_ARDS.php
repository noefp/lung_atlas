<h3>Transcriptome profiling of lungs suffering Acutte Inflammatory Lung Injury (ALI)</h3>
<p>
  <b>Experimental condition:</b> Transcription profile of lungs after treatment with Lipopolysaccharide (LPS) injected into the lungs or injected in the peritoneum.<br>
  <b>Tissue:</b> Lungs.<br>
  <b>Age:</b> 6-8 weeks old.<br>
  <b>Sex:</b> Males.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads lenght:</b> 100 bp<br>
  <b>Sample processing:</b> Total RNA from lung tissue was extracted and the quality of the RNA samples was evaluated. Transcriptome libraried were constructed using mRNA sourced from lung tissue of both control, pulmonary and extra-pulmonary group. Sequencing was performed on the BGISEQ platform and raw reads were normalized to TPM.
</p>

<h4>Experimental Conditions:</h4>
<ul>
    <li>Control_lung: Lungs from mice without LPS treatment(5 biological replicates, with 1 run each one).</li>
    <li>LPS_lung: Lungs from mice with intratracheal injection of LPS (10 mg/kg) treatment  (5 biological replicates, with 1 run each one).</li>
    <li>LPS_extrapulmonary: Lungs from mice with intraperitoneal injection of LPS (25 mg/kg) to construct sepsis-related Acute Respiratory Distress Syndrome (ARDS) (5 biological replicates, with 1 run each one).</li>
</ul>

