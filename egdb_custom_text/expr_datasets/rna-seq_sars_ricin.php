<h3>Transcriptome profiling of lungs in a mice model for severe COVID-19</h3>
<p>
  <b>Experimental condition:</b> Transcription profile of lungs after treatment with ricin, SARS-CoV-2 or both with the objective to show that ricin increases susceptibility to the virus, being an animal model to perform experiments.<br>
  <b>Tissue:</b> Lungs.<br>
  <b>Age:</b> 8-10 weeks old.<br>
  <b>Sex:</b> Females.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads lenght:</b> 100 bp.<br>
  <b>Sample processing:</b> Mice, under deep anesthesia, were employed to in situ infusion of the RNA preserving reagent RNAlater (Thermo Fisher Scientific, Waltham, MA, USA) into the lungs through the trachea. The thoracic cavity was dissected, lungs were removed after PBS perfusion via the heart and placed in a test tube containing RNAlater for 24 h. RNA was isolated from lungs using Qiagen RNeasy mini kits (Qiagen, CA, USA) with an on-column DNase step (Qiagen, CA, USA) according to the manufacturers' instructions. RNA quantification was carried out in a Qubit fluorometer using the Qubit RNA HS assay kit (Invitogen, CA, USA). Quality control analysis of RNA integrity was performed using High Sensitivity RNA ScreenTape and the TapeStation Analysis software (Agilent Technologies). Raw reads were normalized to TPM.
</p>

<h4>Experimental Conditions:</h4>
<ul>
    <li>Control_healthy: Lungs from mice without any treatment (4 biological replicates, with 1 run each one).</li>
    <li>Low_ricin: Lungs from mice mice exposed to low-dose ricin (1.7 µg/kg ricin) only (4 biological replicates, with 1 run each one).</li>
    <li>SARSCoV-2: Lungs from mice infected solely with SARS-CoV-2 (4 biological replicates, with 1 run each one).</li>
    <li>SARSCoV-2_ricin: Lungs from mice exposed to low-dose ricin (1.7 µg/kg ricin) and SARS-CoV-2 infection  (4 biological replicates, with 1 run each one).</li>
  </ul>

